public class Website {
    private String title;
    private String url;
    private String Rss;
    public String getTitle(){
        return title;
    }
    public String getUrl(){
        return url;
    }
    public String getRss(){
        return Rss;
    }
    public void setTitle(String title){
        this.title=title;
    }
    public void setUrl(String Url){
        this.url=Url;
    }
    public void setRss(String Rss){
        this.Rss=Rss;
    }
}
